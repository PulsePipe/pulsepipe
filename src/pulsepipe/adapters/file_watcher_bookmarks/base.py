from abc import ABC, abstractmethod

class BookmarkStore(ABC):
    @abstractmethod
    def is_processed(self, path: str) -> bool:
        pass

    @abstractmethod
    def mark_processed(self, path: str, status: str = "processed"):
        pass

    @abstractmethod
    def get_all(self):
        pass

    @abstractmethod
    def clear_all(self):
        pass
